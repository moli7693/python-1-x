# 任务1：编写一个函数，返回输入的集合中的最大值和最小值
#########################################################################
import math
print('【任务1开始】', '=' * 100)


# 返回集合中的最大值和最小值
# arr：List类型集合
# 返回值：将最大值和最小值同时返回

def find_max_min(arr):
    # 设置最大值和最小值的初值
    min_value = math.inf
    max_value = -math.inf
    for data in arr:            # 遍历每个元素
        # 如果当前元素大于最大值，则将当前元素指定为新的最大值
        if data > max_value:  # ____【1】____:
            max_value = data
        # 如果当前元素小于最小值，则将当前元素指定为新的最小值
        if data < min_value:  # ____【2】____:
            min_value = data
    return max_value, min_value


# 生成一个集合
source = [39, 100, 20, 100, 0, 12, 56, 89, 6, 27]

max, min = find_max_min(source)
print("最大值：%d, 最小值：%d" % (max, min))

print('【任务1结束】', '=' * 100)


