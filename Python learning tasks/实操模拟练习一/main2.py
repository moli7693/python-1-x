# 任务2：检测字符串是否符合要求
# 字符串必须同时符合下列要求：
# (1)长度不少于8个字符
# (2)只能包含英文和数字，不能包含其它字符
# (3)不能只包含数字
#########################################################################
print('【任务2开始】', '=' * 100)

# 判断字符串是否符合要求
# 如果符合所有要求则返回True，否则返回False


def check_string(s):
    if len(s) < 8:  # ____【3】____(s) < 8          # 长度判断
        return False
    if not s.isalnum():     # 字母和数字判断
        return False  # ____【4】____
    if s.isdigit():  # ____【5】____():         # 是否仅包含数字
        return False
    return True  # ____【6】____


test_str = ['Hello123', 'Hello', '12345678', 'Hello@123', '1234.567']
for str in test_str:
    print('字符串【%s】检查结果：%s' % (str, check_string(str)))

print('【任务2结束】', '=' * 100)
